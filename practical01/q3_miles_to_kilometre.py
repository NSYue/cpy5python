# Description: Program to get  a number in miles
# and converts it to kilometres.

#main

#prompt and get number in miles
miles = float(input("Enter the number in miles: "))

#convert miles to kilometers
kilometers = miles*1.60934

#display reslut
print ("Number in kilometers= {0:.3f}".format (kilometers))
